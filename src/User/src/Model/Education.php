<?php


namespace User\Model;

/**
 * Class Education
 * @package User\Model
 */
class Education
{
    private $id;
    private $name;

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @return mixed
     */
    public function getName()
    {
        return $this->name;
    }

}
